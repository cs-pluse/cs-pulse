package org.wso2.cs.pulse.dto;

public class CriteriaDTO {
    private String name;
    private int points;

    public CriteriaDTO(String name, int points) {
        this.name = name;
        this.points = points;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPoints() {
        return points;
    }

    public void setPoints(int points) {
        this.points = points;
    }
}